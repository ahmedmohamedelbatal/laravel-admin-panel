// Start AsideBar Btn 
function open_side() {
	document.getElementById("sidebar").style.display = "block";
	document.getElementById("open-aside").style.display = "none";
	document.getElementById("close-aside").style.display = "block";
}
  
function close_side() {
	document.getElementById("sidebar").style.display = "none";
	document.getElementById("open-aside").style.display = "block";
	document.getElementById("close-aside").style.display = "none";
}
// End AsideBar Btn